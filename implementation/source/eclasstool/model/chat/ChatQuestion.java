package eclasstool.model.chat;

/**
 * For now, a ChatQuestion is just a string. This definition will expand
 * soon.
 * @author Daniel Lee (dlee104@calpoly.edu)
 */
public class ChatQuestion {
   /**
    * The pending question.
    */
   private String question;

   /**
    * Constructs a new question.
    */
   public ChatQuestion(String question) {
      this.question = question;
   }

   /**
    * Retrieves the question.
    * @return the question being asked
    */
   public String getQuestion() {
      return question;
   }
}
