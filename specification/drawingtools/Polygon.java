package drawingtools;

/**
 * Polygon class that extends from shape.
 **/
public abstract class Polygon extends Shape {
   /**
     * Number of sides that the Polygon object has.
     */
   int numSides;
   /**
     * X position of the Polygon object.
     */
   int x_pos;
   /**
     * Y position of the Polygon object.
     */
   int y_pos;
}
