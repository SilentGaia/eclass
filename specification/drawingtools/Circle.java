package drawingtools;
/**
 * Circle class that extends from shape.  Has radius field for size.
 **/
public abstract class Circle extends Shape {
   /** 
    * Radius of the Circle object.
    */
   int radius;
   /**
    * X position of the Circle object's center.
    */
   int x_pos;
   /**
     * Y position of the Circle object's center.
     */
   int y_pos;
}
