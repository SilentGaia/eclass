 package drawingtools;

 public abstract class Line extends Shape  {
   /**
     * X position of the beginning position of the Line object.
     */
   int x_pos;
   /**
     * Y position of the beginning position of the Line object.
     */
   int y_pos;
   /**
     * X position of the ending position of the Line object.
     */
   int x2_pos;
   /**
     * Y position of the ending positon of the Line object.
     */
   int y2_pos;
 }
